using System.Runtime.Serialization;

using Newtonsoft.Json;
using Newtonsoft.Json.Converters;

namespace Guilded.NET.Base.Teams
{
    /// <summary>
    /// A region where voice channel's server is located.
    /// </summary>
    [JsonConverter(typeof(StringEnumConverter))]
    public enum VoiceRegion
    {
        /// <summary>
        /// Central EU (Frankfurt)
        /// </summary>
        [EnumMember(Value = "eu-central-1")]
        EUCentral,
        /// <summary>
        /// Western USA (Oregon)
        /// </summary>
        [EnumMember(Value = "us-west-2")]
        USWest,
        /// <summary>
        /// Eastern USA (Ohio)
        /// </summary>
        [EnumMember(Value = "us-east-2")]
        USEast,
        /// <summary>
        /// Asia Pacific (Sydney)
        /// </summary>
        [EnumMember(Value = "ap-southeast-2")]
        APSydney,
        /// <summary>
        /// South America (Sao Paulo)
        /// </summary>
        [EnumMember(Value = "sa-east-1")]
        SouthAmericaEast
    }
}